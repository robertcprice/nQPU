/// Dispersive readout simulator for a single transmon qubit coupled
/// to a readout resonator.
///
/// Computes IQ centroids from the steady-state resonator response in
/// the dispersive limit, adds Gaussian noise scaled by the readout
/// duration and resonator linewidth, and classifies the result.
#[derive(Clone, Debug)]
pub struct ReadoutSimulator {
    config: ReadoutConfig,
    /// Qubit transition frequency in GHz.
    qubit_freq_ghz: f64,
    /// Dispersive shift chi in GHz (computed from g^2 / Delta).
    chi_ghz: f64,
    /// IQ centroid for qubit in |0>.
    center_0: Complex64,
    /// IQ centroid for qubit in |1>.
    center_1: Complex64,
    /// Noise standard deviation per quadrature.
    noise_sigma: f64,
}

impl ReadoutSimulator {
    /// Create a new readout simulator.
    ///
    /// # Arguments
    /// * `config` - Resonator and readout parameters.
    /// * `qubit_freq_ghz` - Qubit transition frequency in GHz.
    ///
    /// # Physics
    ///
    /// The dispersive shift is chi = g^2 / |Delta| where Delta = qubit_freq - resonator_freq.
    /// IQ centroids come from the resonator steady-state response driven at the bare
    /// resonator frequency:
    ///
    /// ```text
    /// alpha_k = A / (kappa/2 + i * delta_k)
    /// ```
    ///
    /// where delta_0 = -chi, delta_1 = +chi (frequency shifts for |0> and |1>),
    /// and A is a drive-strength factor proportional to g * sqrt(n_photons).
    pub fn new(config: ReadoutConfig, qubit_freq_ghz: f64) -> Self {
        let delta_ghz = (qubit_freq_ghz - config.resonator_freq_ghz).abs();
        // Dispersive shift: chi = g^2 / Delta
        let chi_ghz = if delta_ghz > 1e-12 {
            config.coupling_ghz * config.coupling_ghz / delta_ghz
        } else {
            0.0
        };

        // Convert kappa from MHz to GHz for consistent units.
        let kappa_ghz = config.kappa_mhz * 1e-3;

        // Drive amplitude factor. We assume the readout drive is at the bare
        // resonator frequency with mean photon number n_bar ~ 1. The overall
        // scale A = 2 * g * sqrt(n_bar) is in GHz; we keep it in GHz and
        // normalise to "arbitrary IQ units" later.
        let n_bar: f64 = 1.0;
        let drive_a = 2.0 * config.coupling_ghz * n_bar.sqrt();

        // Steady-state intracavity field for qubit in |0> and |1>.
        // alpha_k = A / (kappa/2 + i * delta_k)
        // Drive at bare resonator freq => detuning from dressed freq is ∓ chi.
        let half_kappa = kappa_ghz / 2.0;
        let denom_0 = c(half_kappa, -chi_ghz); // delta_0 = -chi
        let denom_1 = c(half_kappa, chi_ghz);  // delta_1 = +chi

        let center_0 = c_re(drive_a) / denom_0;
        let center_1 = c_re(drive_a) / denom_1;

        // Noise sigma: shot noise + thermal noise.
        // Shot noise scales as sqrt(kappa * T_readout) in natural units;
        // we add the user-specified thermal noise amplitude.
        let kappa_per_ns = kappa_ghz * 2.0 * PI; // angular rate in rad/ns
        let shot_noise = (kappa_per_ns * config.readout_duration_ns).sqrt();
        // Normalise shot noise to the same IQ scale as the centroids.
        // The centroid magnitude is O(drive_a / kappa); shot noise per quadrature
        // is O(1/sqrt(n_photons * kappa * T)). We scale it relative to the
        // centroid separation so the SNR formula stays clean.
        let separation = (center_1 - center_0).norm();
        let noise_sigma = if separation > 1e-30 {
            // Physical noise: fraction of separation.
            // sqrt(1/(kappa*T)) gives photon-resolved noise relative to signal;
            // we add thermal_noise_amplitude as a baseline.
            let photon_noise = 1.0 / shot_noise.max(1e-30);
            (photon_noise + config.thermal_noise_amplitude) * separation
        } else {
            config.thermal_noise_amplitude
        };

        Self {
            config,
            qubit_freq_ghz,
            chi_ghz,
            center_0,
            center_1,
            noise_sigma,
        }
    }

    /// Return the dispersive shift chi in MHz.
    ///
    /// chi = g^2 / |Delta| converted to MHz.
    pub fn dispersive_shift_mhz(&self) -> f64 {
        self.chi_ghz * 1e3 // GHz -> MHz
    }

    /// Perform a single-shot dispersive readout on the given qubit state.
    ///
    /// The state vector must have length >= 2. Only the first two
    /// amplitudes (computational subspace |0>, |1>) are used.
    ///
    /// # Arguments
    /// * `state` - Qubit statevector (at least 2 amplitudes).
    /// * `seed` - RNG seed for reproducible noise.
    ///
    /// # Returns
    /// A `ReadoutResult` with the noisy IQ point, classified state, and
    /// the Born-rule probability of the classified outcome.
    pub fn readout(&self, state: &[Complex64], seed: u64) -> ReadoutResult {
        assert!(state.len() >= 2, "State must have at least 2 amplitudes");

        let p0 = state[0].norm_sqr();
        let p1 = state[1].norm_sqr();

        let mut rng = Rng::new(seed);

        // Decide which state the qubit is actually in (projective measurement).
        let r = rng.next_f64();
        let (true_state, center, prob) = if r < p0 / (p0 + p1) {
            (0u8, self.center_0, p0)
        } else {
            (1u8, self.center_1, p1)
        };

        // Add Gaussian noise to the IQ point.
        let noise_i = self.noise_sigma * rng.next_gaussian();
        let noise_q = self.noise_sigma * rng.next_gaussian();
        let iq_point = center + c(noise_i, noise_q);

        // Classify: which centroid is the noisy IQ point closer to?
        let dist_0 = (iq_point - self.center_0).norm();
        let dist_1 = (iq_point - self.center_1).norm();
        let classified_state = if dist_0 <= dist_1 { 0 } else { 1 };

        ReadoutResult {
            iq_point,
            classified_state,
            probability: prob,
        }
    }

    /// Perform many readout shots and collect statistics.
    ///
    /// Each shot uses a different RNG seed derived from `seed` to ensure
    /// independence while remaining deterministic.
    ///
    /// # Arguments
    /// * `state` - Qubit statevector.
    /// * `num_shots` - Number of measurement repetitions.
    /// * `seed` - Base RNG seed.
    pub fn batch_readout(
        &self,
        state: &[Complex64],
        num_shots: usize,
        seed: u64,
    ) -> Vec<ReadoutResult> {
        (0..num_shots)
            .map(|i| {
                // Derive per-shot seed via splitmix-style mixing to avoid
                // correlated streams.
                let shot_seed = seed
                    .wrapping_add(i as u64)
                    .wrapping_mul(0x9e3779b97f4a7c15)
                    ^ (i as u64).wrapping_mul(0x517cc1b727220a95);
                self.readout(state, shot_seed)
            })
            .collect()
    }

    /// Generate a discrimination report by probing the readout with
    /// ideal |0> and |1> states.
    ///
    /// This mirrors the calibration procedure performed on real hardware:
    /// prepare |0> N times, prepare |1> N times, build the IQ clouds,
    /// and compute assignment fidelity.
    ///
    /// # Arguments
    /// * `num_shots` - Shots per state (total shots = 2 * num_shots).
    /// * `seed` - Base RNG seed.
    pub fn discrimination_analysis(
        &self,
        num_shots: usize,
        seed: u64,
    ) -> DiscriminationReport {
        let state_0 = [ONE, ZERO];
        let state_1 = [ZERO, ONE];

        let results_0 = self.batch_readout(&state_0, num_shots, seed);
        let results_1 = self.batch_readout(&state_1, num_shots, seed.wrapping_add(0xDEADBEEF));

        // Compute empirical centroids from the IQ clouds.
        let mean_0 = iq_mean(&results_0);
        let mean_1 = iq_mean(&results_1);

        let separation = (mean_1 - mean_0).norm();

        // Standard deviations of each cloud (averaged across I and Q).
        let std_0 = iq_std(&results_0, mean_0);
        let std_1 = iq_std(&results_1, mean_1);
        let avg_std = (std_0 + std_1) / 2.0;

        let snr = if avg_std > 1e-30 {
            separation / avg_std
        } else {
            f64::INFINITY
        };

        // Assignment fidelity: F = 1 - (P(1|0) + P(0|1)) / 2
        let misclass_0 = results_0
            .iter()
            .filter(|r| r.classified_state != 0)
            .count() as f64
            / num_shots as f64;
        let misclass_1 = results_1
            .iter()
            .filter(|r| r.classified_state != 1)
            .count() as f64
            / num_shots as f64;
        let assignment_fidelity = 1.0 - (misclass_0 + misclass_1) / 2.0;

        DiscriminationReport {
            center_0: mean_0,
            center_1: mean_1,
            separation,
            snr,
            assignment_fidelity,
        }
    }

    /// Access the precomputed IQ centroid for |0>.
    pub fn iq_center_0(&self) -> Complex64 {
        self.center_0
    }

    /// Access the precomputed IQ centroid for |1>.
    pub fn iq_center_1(&self) -> Complex64 {
        self.center_1
    }

    /// Access the noise standard deviation per quadrature.
    pub fn noise_sigma(&self) -> f64 {
        self.noise_sigma
    }
}

// ============================================================
// HELPER FUNCTIONS
// ============================================================

/// Mean IQ point from a batch of readout results.
fn iq_mean(results: &[ReadoutResult]) -> Complex64 {
    if results.is_empty() {
        return ZERO;
    }
    let sum: Complex64 = results.iter().map(|r| r.iq_point).sum();
    sum / c_re(results.len() as f64)
}

/// Standard deviation of IQ cloud (RMS distance from centroid).
fn iq_std(results: &[ReadoutResult], mean: Complex64) -> f64 {
    if results.len() < 2 {
        return 0.0;
    }
    let var: f64 = results
        .iter()
        .map(|r| (r.iq_point - mean).norm_sqr())
        .sum::<f64>()
        / (results.len() - 1) as f64;
    var.sqrt()
}

