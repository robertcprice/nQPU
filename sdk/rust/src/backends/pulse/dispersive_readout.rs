#[cfg(test)]
mod tests {
    use super::*;

    const TOL: f64 = 1e-3;

    fn default_config() -> ReadoutConfig {
        ReadoutConfig {
            resonator_freq_ghz: 7.0,
            coupling_ghz: 0.1,
            kappa_mhz: 2.0,
            readout_duration_ns: 1000.0,
            thermal_noise_amplitude: 0.05,
        }
    }

    // ----------------------------------------------------------
    // 1. Dispersive shift matches g^2 / Delta
    // ----------------------------------------------------------
    #[test]
    fn test_dispersive_shift_matches_g_squared_over_delta() {
        let config = default_config();
        let qubit_freq = 5.0; // GHz
        let sim = ReadoutSimulator::new(config.clone(), qubit_freq);

        let delta = (qubit_freq - config.resonator_freq_ghz).abs(); // 2.0 GHz
        let expected_chi_ghz = config.coupling_ghz * config.coupling_ghz / delta; // 0.01 / 2.0 = 0.005 GHz
        let expected_chi_mhz = expected_chi_ghz * 1e3; // 5.0 MHz

        let actual = sim.dispersive_shift_mhz();
        assert!(
            (actual - expected_chi_mhz).abs() < TOL,
            "Dispersive shift: expected {:.4} MHz, got {:.4} MHz",
            expected_chi_mhz,
            actual
        );
    }

    // ----------------------------------------------------------
    // 2. Dispersive shift scales with coupling squared
    // ----------------------------------------------------------
    #[test]
    fn test_dispersive_shift_scales_with_coupling() {
        let qubit_freq = 5.0;
        let mut config1 = default_config();
        config1.coupling_ghz = 0.05;
        let sim1 = ReadoutSimulator::new(config1, qubit_freq);

        let mut config2 = default_config();
        config2.coupling_ghz = 0.10;
        let sim2 = ReadoutSimulator::new(config2, qubit_freq);

        // Doubling g should quadruple chi.
        let ratio = sim2.dispersive_shift_mhz() / sim1.dispersive_shift_mhz();
        assert!(
            (ratio - 4.0).abs() < 0.01,
            "chi should scale as g^2: expected ratio 4.0, got {:.4}",
            ratio
        );
    }

    // ----------------------------------------------------------
    // 3. Readout classifies |0> correctly
    // ----------------------------------------------------------
    #[test]
    fn test_readout_classifies_ground_state() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);

        let state_0 = [ONE, ZERO];
        let result = sim.readout(&state_0, 42);

        assert_eq!(
            result.classified_state, 0,
            "Ground state should be classified as 0"
        );
        assert!(
            (result.probability - 1.0).abs() < TOL,
            "Probability should be ~1.0 for pure |0>"
        );
    }

    // ----------------------------------------------------------
    // 4. Readout classifies |1> correctly
    // ----------------------------------------------------------
    #[test]
    fn test_readout_classifies_excited_state() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);

        let state_1 = [ZERO, ONE];
        let result = sim.readout(&state_1, 42);

        assert_eq!(
            result.classified_state, 1,
            "Excited state should be classified as 1"
        );
        assert!(
            (result.probability - 1.0).abs() < TOL,
            "Probability should be ~1.0 for pure |1>"
        );
    }

    // ----------------------------------------------------------
    // 5. IQ centroids are distinct (separation > 0)
    // ----------------------------------------------------------
    #[test]
    fn test_iq_centroids_separated() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);

        let sep = (sim.iq_center_1() - sim.iq_center_0()).norm();
        assert!(
            sep > 1e-6,
            "IQ centroids should be well-separated, got separation = {:.6e}",
            sep
        );
    }

    // ----------------------------------------------------------
    // 6. Batch readout statistics match expected distribution
    // ----------------------------------------------------------
    #[test]
    fn test_batch_readout_statistics() {
        let config = ReadoutConfig {
            thermal_noise_amplitude: 0.01, // Low noise for clean statistics
            ..default_config()
        };
        let sim = ReadoutSimulator::new(config, 5.0);

        // Superposition: |+> = (|0> + |1>) / sqrt(2)
        let s = 1.0 / 2.0f64.sqrt();
        let state_plus = [c_re(s), c_re(s)];

        let results = sim.batch_readout(&state_plus, 10_000, 12345);
        let count_0 = results.iter().filter(|r| r.classified_state == 0).count();
        let count_1 = results.len() - count_0;

        let frac_0 = count_0 as f64 / results.len() as f64;
        let frac_1 = count_1 as f64 / results.len() as f64;

        // Should be close to 50/50 (within ~3% for 10k shots).
        assert!(
            (frac_0 - 0.5).abs() < 0.05,
            "Expected ~50% |0>, got {:.1}%",
            frac_0 * 100.0
        );
        assert!(
            (frac_1 - 0.5).abs() < 0.05,
            "Expected ~50% |1>, got {:.1}%",
            frac_1 * 100.0
        );
    }

    // ----------------------------------------------------------
    // 7. Discrimination analysis produces SNR > 1
    // ----------------------------------------------------------
    #[test]
    fn test_discrimination_snr_above_one() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);

        let report = sim.discrimination_analysis(5_000, 99);

        assert!(
            report.snr > 1.0,
            "SNR should be > 1 for reasonable parameters, got {:.3}",
            report.snr
        );
        assert!(
            report.separation > 1e-6,
            "Separation should be positive, got {:.6e}",
            report.separation
        );
    }

    // ----------------------------------------------------------
    // 8. Assignment fidelity exceeds 0.9 with good parameters
    // ----------------------------------------------------------
    #[test]
    fn test_assignment_fidelity_reasonable() {
        let config = ReadoutConfig {
            thermal_noise_amplitude: 0.02, // Low noise
            readout_duration_ns: 2000.0,   // Long integration
            ..default_config()
        };
        let sim = ReadoutSimulator::new(config, 5.0);

        let report = sim.discrimination_analysis(5_000, 77);

        assert!(
            report.assignment_fidelity > 0.9,
            "Assignment fidelity should exceed 0.9, got {:.4}",
            report.assignment_fidelity
        );
    }

    // ----------------------------------------------------------
    // 9. Noise sigma is positive
    // ----------------------------------------------------------
    #[test]
    fn test_noise_sigma_positive() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);
        assert!(
            sim.noise_sigma() > 0.0,
            "Noise sigma should be positive, got {}",
            sim.noise_sigma()
        );
    }

    // ----------------------------------------------------------
    // 10. Deterministic: same seed produces same result
    // ----------------------------------------------------------
    #[test]
    fn test_readout_deterministic_with_same_seed() {
        let config = default_config();
        let sim = ReadoutSimulator::new(config, 5.0);

        let s = 1.0 / 2.0f64.sqrt();
        let state = [c_re(s), c_re(s)];

        let r1 = sim.readout(&state, 42);
        let r2 = sim.readout(&state, 42);

        assert_eq!(
            r1.iq_point, r2.iq_point,
            "Same seed should produce identical IQ points"
        );
        assert_eq!(
            r1.classified_state, r2.classified_state,
            "Same seed should produce identical classification"
        );
    }

    // ----------------------------------------------------------
    // 11. Zero coupling gives zero dispersive shift
    // ----------------------------------------------------------
    #[test]
    fn test_zero_coupling_zero_shift() {
        let config = ReadoutConfig {
            coupling_ghz: 0.0,
            ..default_config()
        };
        let sim = ReadoutSimulator::new(config, 5.0);
        assert!(
            sim.dispersive_shift_mhz().abs() < 1e-12,
            "Zero coupling should give zero dispersive shift"
        );
    }
}
