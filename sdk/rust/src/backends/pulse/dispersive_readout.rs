//! Dispersive Readout Simulation for Superconducting Qubits
//!
//! Models the dispersive readout process used in transmon-based quantum
//! processors. In the dispersive regime (detuning >> coupling), the readout
//! resonator acquires a qubit-state-dependent frequency shift (dispersive
//! shift chi = g^2 / Delta). Measuring the transmitted/reflected microwave
//! signal produces IQ-plane data whose location encodes the qubit state.
//!
//! # Physical Model
//!
//! The dispersive Hamiltonian in the rotating frame of the resonator:
//!
//! ```text
//! H_disp = chi * a†a * sigma_z / 2
//! ```
//!
//! The resonator response to a readout drive at frequency omega_d:
//!
//! ```text
//! alpha_k = 2 * g * sqrt(n_bar) / (kappa + 2i*(omega_r - omega_d ± chi))
//! ```
//!
//! where k in {0, 1} labels the qubit state, kappa is the resonator linewidth,
//! and the sign of chi depends on the qubit state.
//!
//! IQ separation determines readout fidelity; Gaussian noise from photon
//! shot noise and thermal fluctuations limits discrimination.
//!
//! # Example
//!
//! ```rust,ignore
//! use nqpu_metal::dispersive_readout::*;
//! use num_complex::Complex64;
//!
//! let config = ReadoutConfig {
//!     resonator_freq_ghz: 7.0,
//!     coupling_ghz: 0.1,
//!     kappa_mhz: 2.0,
//!     readout_duration_ns: 1000.0,
//!     thermal_noise_amplitude: 0.05,
//! };
//! let sim = ReadoutSimulator::new(config, 5.0);
//! let chi = sim.dispersive_shift_mhz();
//! assert!(chi > 0.0);
//!
//! // Readout of |0> state
//! let state_0 = vec![Complex64::new(1.0, 0.0), Complex64::new(0.0, 0.0)];
//! let result = sim.readout(&state_0, 42);
//! assert_eq!(result.classified_state, 0);
//! ```

use num_complex::Complex64;
use std::f64::consts::PI;

// ============================================================
// COMPLEX NUMBER HELPERS (local to this module)
// ============================================================

const ZERO: Complex64 = Complex64 { re: 0.0, im: 0.0 };
const ONE: Complex64 = Complex64 { re: 1.0, im: 0.0 };

#[inline]
fn c(re: f64, im: f64) -> Complex64 {
    Complex64::new(re, im)
}

#[inline]
fn c_re(re: f64) -> Complex64 {
    Complex64::new(re, 0.0)
}

// ============================================================
// READOUT CONFIGURATION
// ============================================================

/// Configuration parameters for the dispersive readout resonator.
///
/// These map to experimentally calibrated device parameters.
/// Typical IBM transmon values: resonator ~ 6.5-7.5 GHz, coupling ~ 50-150 MHz,
/// kappa ~ 1-5 MHz, readout duration ~ 500-2000 ns.
#[derive(Clone, Debug)]
pub struct ReadoutConfig {
    /// Readout resonator frequency in GHz.
    pub resonator_freq_ghz: f64,
    /// Qubit-resonator coupling strength g in GHz.
    pub coupling_ghz: f64,
    /// Resonator linewidth (decay rate) kappa in MHz.
    pub kappa_mhz: f64,
    /// Total readout integration time in nanoseconds.
    pub readout_duration_ns: f64,
    /// Amplitude of additive Gaussian noise per quadrature.
    /// Models thermal + amplifier noise. Typical range: 0.01 - 0.2.
    pub thermal_noise_amplitude: f64,
}

impl Default for ReadoutConfig {
    fn default() -> Self {
        Self {
            resonator_freq_ghz: 7.0,
            coupling_ghz: 0.1,
            kappa_mhz: 2.0,
            readout_duration_ns: 1000.0,
            thermal_noise_amplitude: 0.05,
        }
    }
}

// ============================================================
// READOUT RESULT
// ============================================================

/// Result of a single-shot dispersive readout measurement.
#[derive(Clone, Debug)]
pub struct ReadoutResult {
    /// Measured IQ point (I = real, Q = imaginary) in arbitrary units.
    pub iq_point: Complex64,
    /// Classified qubit state (0 or 1) based on IQ discrimination.
    pub classified_state: u8,
    /// Born-rule probability of the classified state before measurement.
    pub probability: f64,
}

// ============================================================
// DISCRIMINATION REPORT
// ============================================================

/// Statistical report characterizing the readout discriminator.
///
/// Generated by injecting known |0> and |1> states many times and
/// computing the IQ cloud statistics.
#[derive(Clone, Debug)]
pub struct DiscriminationReport {
    /// IQ centroid for the |0> state cloud.
    pub center_0: Complex64,
    /// IQ centroid for the |1> state cloud.
    pub center_1: Complex64,
    /// Euclidean distance between centroids in IQ plane.
    pub separation: f64,
    /// Signal-to-noise ratio: separation / average cloud standard deviation.
    pub snr: f64,
    /// Assignment fidelity: fraction of shots correctly classified.
    /// F_assign = 1 - (P(1|0) + P(0|1)) / 2
    pub assignment_fidelity: f64,
}

// ============================================================
// SEEDED PRNG (xoshiro256** for reproducibility without external deps)
// ============================================================

/// Minimal xoshiro256** PRNG for deterministic, seeded noise generation.
/// We avoid pulling in `rand` thread-level state so that tests are
/// reproducible with an explicit seed.
struct Rng {
    s: [u64; 4],
}

impl Rng {
    fn new(seed: u64) -> Self {
        // SplitMix64 to expand a single seed into 256 bits of state.
        let mut z = seed;
        let mut s = [0u64; 4];
        for slot in &mut s {
            z = z.wrapping_add(0x9e3779b97f4a7c15);
            let mut v = z;
            v = (v ^ (v >> 30)).wrapping_mul(0xbf58476d1ce4e5b9);
            v = (v ^ (v >> 27)).wrapping_mul(0x94d049bb133111eb);
            *slot = v ^ (v >> 31);
        }
        Self { s }
    }

    fn next_u64(&mut self) -> u64 {
        let result = (self.s[1].wrapping_mul(5)).rotate_left(7).wrapping_mul(9);
        let t = self.s[1] << 17;
        self.s[2] ^= self.s[0];
        self.s[3] ^= self.s[1];
        self.s[1] ^= self.s[2];
        self.s[0] ^= self.s[3];
        self.s[2] ^= t;
        self.s[3] = self.s[3].rotate_left(45);
        result
    }

    /// Uniform f64 in [0, 1).
    fn next_f64(&mut self) -> f64 {
        (self.next_u64() >> 11) as f64 / (1u64 << 53) as f64
    }

    /// Standard normal via Box-Muller transform.
    fn next_gaussian(&mut self) -> f64 {
        loop {
            let u1 = self.next_f64();
            let u2 = self.next_f64();
            if u1 > 1e-30 {
                return (-2.0 * u1.ln()).sqrt() * (2.0 * PI * u2).cos();
            }
        }
    }
}

